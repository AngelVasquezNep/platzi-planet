<template lang="pug">
  div
    p.alertMegusta(v-if="megusta") A ti y a toda la Galaxia les gusta esta imagen
    
    .link(v-show="mostrarLink")
      p Este es el link de la imagen, compartelo con tus amigos 
      p {{this.item.links[0].href}}

    .botones
      input(type="button", 
            value="me gusta", 
            title="Me gusta",
            @click="addMegusta", 
            :class="{megustaOk: megusta}")  

      input(type="button", 
            value="comentar", 
            title="Deja tu comentario",
            @click="coment") 

      input(type="button", 
            value="compartir", 
            title="Obten el link de la imagen",
            @click="compartir")

    .comentarios(v-show="mostrarComentario") 

      div(v-for="(com, index) in comentariohecho", class="newComent") 
        p {{com.text}} 
        p(class="horario") Realizado a las: {{com.hora}}  
        span(class="cancel", 
             @click="removeComent(index)",
             title="Eliminar comentario" ) x
      
      input(type="text", 
            v-model="newComent.text", 
            placeholder="Agregue su comentario",
            @keyup.enter="addComentario")
      
      input.enviar(type="button", 
            value="enviar", 
            @click="addComentario",
            @keyup.enter="addComentario")
</template>

<script>
  export default {
    name: 'Megusta',
    props:['item'],
    data(){
      return{
        mostrarComentario: false,
        comentariohecho: [],
        newComent:{text:'', hora:0},
        mostrarLink: false,
        megusta: false
      }
    },
    methods:{
      coment(){
        if(this.mostrarComentario == true) {
          this.mostrarComentario = false 
        } else if(this.mostrarComentario == false) {
          this.mostrarComentario = true
        }
      },
      addComentario(){
        if(this.newComent.text != ''){
          let hoy = new Date()
          this.newComent.hora = `${hoy.getHours()}:${hoy.getMinutes()}`
          this.comentariohecho.push(this.newComent)
          this.newComent = {text:'', hora:0}
        }
      },
      compartir(){
        if(this.mostrarLink) return this.mostrarLink = false
        if(!this.mostrarLink) return this.mostrarLink = true
      },
      removeComent(index){
        this.comentariohecho.splice(index,1)
      },
      addMegusta(){
        this.megusta = true
        console.log("It's ok" + this.megusta )
      }
    }
  }  
</script>

